export const reviews: Review[] = [];

export function getTradeBySlug(slug: string): Trade | undefined {
  return trades.find((t) => t.slug === slug);
}

export function getToolBySlug(slug: string): Tool | undefined {
  return tools.find((t) => t.slug === slug);
}

export function getToolsForTrade(tradeId: string): Tool[] {
  return tools.filter((t) => t.tradeIds.includes(tradeId));
}

export function getToolRankForTrade(toolId: string, tradeId: string): number {
  const tradeTools = getToolsForTrade(tradeId);
  const sorted = [...tradeTools].sort((a, b) => b.rating - a.rating);
  return sorted.findIndex((t) => t.id === toolId) + 1;
}

export function getReviewsForTool(toolId: string): Review[] {
  return reviews.filter((r) => r.toolId === toolId);
}

export function getTopRatedTools(limit = 6): Tool[] {
  return [...tools].sort((a, b) => b.rating - a.rating).slice(0, limit);
}
